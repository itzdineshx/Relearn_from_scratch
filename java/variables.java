//04 variables
import java.lang.*;

class variables{
	public static void main(String args[]){
		//string
		String name= "java";
		System.out.println("My Name is "+name); 
		
		//integer
		int age= 30;
		System.out.println("My Age is "+age);
		
		//float
		float revenue= 1.5f	;
		System.out.println("My Revenue is "+revenue);
		
		//character
		char letter='j';
		System.out.println("My First letter is "+letter);
		
		//Boolean
		boolean good=true;
		System.out.println("Am I Good? "+good);
	}
}